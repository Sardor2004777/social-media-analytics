# ---------- 2FA setup is idempotent on GET ----------

def test_two_factor_setup_creates_secret(client, user):
    """Visiting /2fa/ when no secret exists should generate one."""
    resp = client.get(reverse("accounts:two_factor"))
    assert resp.status_code == 200
    user.refresh_from_db()
    assert user.totp_secret  # got generated
    assert not user.totp_enabled


def test_two_factor_disable(client, user):
    """Disable wipes the secret + flag."""
    user.totp_secret  = "JBSWY3DPEHPK3PXP"
    user.totp_enabled = True
    user.save()

    resp = client.post(reverse("accounts:two_factor"), {"action": "disable"})
    assert resp.status_code == 200
    user.refresh_from_db()
    assert user.totp_enabled is False
    # Empty secret means a fresh one was auto-generated by the view since
    # 2FA is now off — that's expected, not an error.
